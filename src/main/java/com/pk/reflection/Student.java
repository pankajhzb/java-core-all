package com.pk.reflection;

public class Student {
    String sId;
    String sName;

    public String getsId() {
        return sId;
    }

    public String getsName() {
        return sName;
    }

    public void setsId(String sId) {
        this.sId = sId;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }
}
